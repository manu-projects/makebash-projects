## Muestra un Calendario

# mostrar el mes actual del calendario (mes actual, año actual)
cal

# mostrar un mes del calendario (mes=1, año=2023)
cal 1 2023

# mostrar del año actual.. el mes de Junio
cal -m June

# mostrar del año actual.. el mes anterior, el actual y el siguiente
cal -3

# mostrar calendario completo del año actual
cal -y

# mostrar calendario completo (año=2050)
cal 2050

