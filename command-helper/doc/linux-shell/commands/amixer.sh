## Controla el volumen del sistema

# modificar el volumen principal al 50%
# (usar %+ y %- para incrementar y decrementar el valor actual)
amixer set Master 50%

# sube el volumen principal en un 50%
amixer set Master 50%+

# baja el volumen principal en un 50%
amixer set Master 50%-

# silencia el volumen principal
amixer set Master mute

# saca el silencio del volumen principal
amixer set Master unmute

